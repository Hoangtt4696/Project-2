import { toast } from '../../util/toastMessage';

// Import Actions
import { TOGGLE_ADD_POST, SHOW_TOAST } from './AppActions';

// Initial State
const initialState = {
  showAddPost: false,
};

const AppReducer = (state = initialState, action) => {
  switch (action.type) {
    case TOGGLE_ADD_POST:
      return {
        showAddPost: !state.showAddPost,
      };

    case SHOW_TOAST:
      toast({ ...action.toast });
      return state;

    default:
      return state;
  }
};

/* Selectors */

// Get showAddPost
export const getShowAddPost = state => state.app.showAddPost;

// Export Reducer
export default AppReducer;
